function mergeProjectFaithSlides(value = {}) {
  const saved = value.projectFaithSlidesByProject;
  if (saved && typeof saved === "object" && !Array.isArray(saved)) {
    return Object.fromEntries([
      ...Object.entries(saved).filter(([, slides]) => Array.isArray(slides)),
      ...Object.entries(DEFAULT_PROJECT_FAITH_SLIDES).map(([projectId, defaults]) => (
        [projectId, Array.isArray(saved[projectId]) ? saved[projectId] : defaults]
      )),
    ]);
  }

  // Preserve slides from the older single-list version inside Welfare.
  const legacySlides = Array.isArray(value.projectFaithSlides) ? value.projectFaithSlides : null;
  return {
    ...DEFAULT_PROJECT_FAITH_SLIDES,
    welfare: legacySlides || DEFAULT_PROJECT_FAITH_SLIDES.welfare,
  };
}

export function mergeSiteSettings(value = {}) {
  return {
    ...DEFAULT_SITE_SETTINGS,
    ...value,
    colors: {
      ...DEFAULT_SITE_SETTINGS.colors,
      ...value.colors,
    },
    socialLinks: Array.isArray(value.socialLinks) ? value.socialLinks : DEFAULT_SITE_SETTINGS.socialLinks,
    paymentMethods: Array.isArray(value.paymentMethods) ? value.paymentMethods : DEFAULT_SITE_SETTINGS.paymentMethods,
    homeHeroSlides: Array.isArray(value.homeHeroSlides) ? value.homeHeroSlides : DEFAULT_SITE_SETTINGS.homeHeroSlides,
    homeReelSlides: Array.isArray(value.homeReelSlides) ? value.homeReelSlides : DEFAULT_SITE_SETTINGS.homeReelSlides,
    projectProfilesByProject: value.projectProfilesByProject && typeof value.projectProfilesByProject === "object"
      ? value.projectProfilesByProject
      : {},
    projectFaithSlidesByProject: mergeProjectFaithSlides(value),
  };
}
